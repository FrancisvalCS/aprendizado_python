# int, float, bool


# função python que verifica o tipo
print(type('Otávio'))
print(type(1))
print(type(10), type(1.2))

# int -> numeros inteiros
print(11)   
print(-11)
print(0)

# float -> nummeros com casas decimais
print(1.1)

# bool -> verdadeiro ou falso
# **(==) -> conferem se um valor é igual a outro valor e se eles são do mesmo tipo.
print(10 == 10) 
print(10 == 11)

# Conversão de tipos
print(int('1'))
print(int('2') + 2)
print(bool(' '))
print(str(11) + 'b')

